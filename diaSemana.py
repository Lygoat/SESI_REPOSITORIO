diaSmn = int(input("Digite um número de 1 a 7: "))

if diaSmn == 1:
    print("Domingo")
elif diaSmn == 2:
    print("Segunda-feira")
elif diaSmn == 3:
    print("Terça-feira")
elif diaSmn == 4:
    print("Quarta-feira")
elif diaSmn == 5:
    print("Quinta-feira")
elif diaSmn == 6:
    print("Sexta-feira")
elif diaSmn == 7:
    print("Sábado")

else:
    print("Erro: o número digitado não condiz com nenhum dos listados acima.")
